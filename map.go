func (m *MapRegion) Debug() {
	for _, r := range m.Grid {
		for _, p := range r {
			if p == "" {
				fmt.Print("    ")
			} else {
				s := fmt.Sprintf("[%s%s]", p, X)
				fmt.Print(s)
			}
		}
		fmt.Println()
	}
}
